import java.util.*;

class multiplication{
	public static void main(String[] args){
		int number;
		Scanner in = new Scanner(System.in);
		number = in.nextInt();
		for(int i=1 ; i<=10; i++){
			System.out.println(number +" * " +i +" = " +(number * i));
		}
	in.close();




}




}